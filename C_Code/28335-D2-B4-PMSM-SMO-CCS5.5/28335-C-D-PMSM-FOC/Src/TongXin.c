Uint16 crc16_byte(Uint16 crc, Uint16 data)
{
   return (crc >> 8) ^ crc16_table[(crc ^ data) & 0xff];
}

Uint16 CRC_16(Uint16 *DATA,Uint16 N)
{

	Uint16 crc;		//初值 
	crc=0xffff;
	while (N--)
	{
		crc = crc16_byte(crc, *DATA++);
	}
	return crc;

}








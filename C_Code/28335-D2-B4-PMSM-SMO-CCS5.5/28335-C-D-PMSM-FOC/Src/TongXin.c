void LuBo(_iq Ch1,_iq Ch2,_iq Ch3,_iq Ch4)//参数都是标么值 Q格式
{
    static Uint16 N=0;
    Uint16 temp=0;

    if((N<500)&&(LuBo_OK==0))//每个通道500个点 共2000个点
    {
    
    temp=_IQtoF(_IQmpy(Ch1, _IQ(100)))+128; 
    RS232_TX[N]=temp;

    temp=_IQtoF(_IQmpy(Ch2, _IQ(100)))+128; 
  
    RS232_TX[500+N]=temp;

    temp=_IQtoF(_IQmpy(Ch3, _IQ(100)))+128; 
    
    RS232_TX[1000+N]=temp;

    temp=_IQtoF(_IQmpy(Ch4, _IQ(100)))+128; 
  
    RS232_TX[1500+N]=temp;


    N=N+1;
    }
    else
    {
        N=0;
        LuBo_OK=1;//录波完成，去发送啦

    }


}

void TX232_ChuLi(void)
{
   Uint16 CRC=0;
    if(LuBo_OK==1)
    {
        CRC=CRC_16(RS232_TX , 2000);
        RS232_TX[2000]=CRC&0x00ff;//传低字节;
        RS232_TX[2001]=CRC>>8;//先传高字节;
     //上显参数准备完毕，下面开始发送
     RS232_SendBytes(RS232_TX,2002); 

     //发送完毕  //等待上位机处理
     
     LuBo_OK=0;//又去录波了

    }

}

	
